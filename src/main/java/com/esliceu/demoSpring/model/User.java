package com.esliceu.demoSpring.model;

public class User {
    private String Name;
    private int year;

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }
}
